from sendemail import SendEmail

sender = None

def handler():
    
    global sender

    if sender is None:
        from omeroweb.feedback.models import EmailToSend
        counter = EmailToSend.objects.count()
        if counter > 0:
            sender = SendEmail()
    else:
        if sender.allow_thread_timeout:
            sender = None
            return handler()
    return sender